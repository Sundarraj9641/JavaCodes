import java.util.LinkedList;

public class LinkedListDemo {

	public static void main(String[] args) {
		
		//LinkedList
		LinkedList<Integer> l1 = new LinkedList<Integer>();
		l1.add(3);		//add elements in linkedList
		l1.add(2);
		l1.add(7);
		l1.add(3);
		l1.add(9);
		System.out.println(l1.peek());  //Peek() is used to get first element
		System.out.println(l1);  //Peek() is used to get first element

	}

}
